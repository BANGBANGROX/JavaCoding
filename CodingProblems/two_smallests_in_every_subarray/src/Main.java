class Solution {
    public int pairWithMaxSum(int[] arr) {
        // code here
        int n = arr.length;
        int answer = -1;

        for (int i = 1; i < n; ++i) {
            answer = Math.max(answer, arr[i - 1] + arr[i]);
        }

        return answer;
    }
}
